#字典是一种无序的映射的集合，包含一系列的“键:值”对。
#字典常量用花括号表示，例如：{'name':'John','age':25,'sex':'male'}。
#其中，字符串name、age和sex为键，字符串John和sex以及数字25为值。

#主要特点：
#字典的键通常采用字符串，但也可以用数字、元组等不可变的类型。
#字典值可以是任意类型。
#字典也可称为关联数组或散列表，它通过键映射到值。字典是无序的，它通过键
#来索引映射的值，而不是通过位置来索引。
#字典属于可变映射，通过索引来修改键映射的值。
#字典长度可变，可为字典添加或删除“键:值”对。
#字典可以任意嵌套，即键映射的值可以是一个字典。
#字典存储的是对象的引用，而不是对象本身。

#from test3 import * #使当前模块变量引用模块中的正数对象100和列表对象[10,20]
#print(x,y)#输出结果显示确实引用了模块中的对象
#x=200#赋值，使当前模块变量x引用整数对象200，断开与原来的引用
#y[0]=['abd']#修改第一个列表元素，此时修改了模块中的列表对象

#在使用import导入模块时，模块中的变量名使用“模块名.”
#作为限定词，所以不存在歧义，即使与其他模块变量同名也没有关系。

#在使用from时，当前模块的同名变量引用了模块内部的对象。

#在遇到与当前模块或其他模块变量同名时，使用时应特别小心。

#使用模块内的可修改对象
#使用from导入模块时，可以直接使用变量名引用模块中的对象，
#避免了输入“模块名.”作为限定词。这种便利有时也会遇到麻烦。
